        C = [405, 293, 81]

        if active_set:
            (P, iter_count, total_price, total_power, total_power_loss) = algorithm.run_algorithm(
                P_min, P_max, p_load, p_loss, A, B, C
            )   
        if least_sq:
            (P, iter_count, total_price, total_power, total_power_loss) = least_squares.run_algorithm(
                P_min, P_max, p_load, p_loss, A, B, C
            ) 

        print("P =", P)
